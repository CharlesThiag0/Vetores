package testeVector6;

public class ValorDouble {
	private double numero;
	private int posicao;
	
	public ValorDouble (double numero, int posicao) {
		this.numero = numero;
		this.posicao = posicao;
	}
	
	public double getNumero() {
		return this.numero;
	}
	
	public int getPosicao() {
		return this.posicao;
	}
	
}
